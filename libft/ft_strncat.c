#include "libft.h"

char	*ft_strncat(char *s1, char const *s2, size_t n)
{
	char *temp;

	temp = s1;
	while (*s1)
		s1++;
	while (n && (*s1++ = *s2++))
		n--;
	if (!n)
		*s1 = '\0';
	return (temp);
}
